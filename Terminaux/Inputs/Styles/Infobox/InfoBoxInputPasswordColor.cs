        /// <param name="InfoBoxTitledColor">InfoBoxTitled color</param>
        /// <param name="BackgroundColor">InfoBoxTitled background color</param>
        /// <param name="text">Text to be written.</param>
        /// <param name="useColor">Whether to use color or not</param>
        /// <param name="vars">Variables to format the message before it's written.</param>
        internal static string WriteInfoBoxInputPasswordColorBack(string title, string text,
                                       char UpperLeftCornerChar, char LowerLeftCornerChar, char UpperRightCornerChar, char LowerRightCornerChar,
                                       char UpperFrameChar, char LowerFrameChar, char LeftFrameChar, char RightFrameChar,
                                       Color InfoBoxTitledColor, Color BackgroundColor, bool useColor, params object[] vars)
        {
            bool initialCursorVisible = ConsoleWrapper.CursorVisible;
            bool initialScreenIsNull = ScreenTools.CurrentScreen is null;
            var infoBoxScreenPart = new ScreenPart();
            var screen = new Screen();
            if (initialScreenIsNull)
                ScreenTools.SetCurrent(screen);
            ScreenTools.CurrentScreen?.AddBufferedPart(nameof(InfoBoxInputPasswordColor), infoBoxScreenPart);
            try
            {
                int rightMargin = 0;
                int currIdx = 0;
                int increment = 0;
                bool exiting = false;
                bool delay = false;
                infoBoxScreenPart.AddDynamicText(() =>
                {
                    // Deal with the lines to actually fit text in the infobox
                    string[] splitFinalLines = InfoBoxColor.GetFinalLines(text, vars);
                    var (maxWidth, maxHeight, _, borderX, borderY) = InfoBoxColor.GetDimensionsInput(splitFinalLines);

                    // Fill the info box with text inside it
                    var boxBuffer = new StringBuilder(
                        InfoBoxColor.RenderTextInput(0, title, text, UpperLeftCornerChar, LowerLeftCornerChar, UpperRightCornerChar, LowerRightCornerChar, UpperFrameChar, LowerFrameChar, LeftFrameChar, RightFrameChar, InfoBoxTitledColor, BackgroundColor, useColor, ref increment, ref delay, ref exiting, currIdx, false, vars)
                    );

                    // Write the input bar and set the cursor position
                    int inputPosX = borderX + 3;
                    rightMargin = inputPosX;
                    int inputPosY = borderY + maxHeight - 3;
                    int maxInputWidth = maxWidth - 6;
                    if (useColor)
                    {
                        boxBuffer.Append(
                            BorderColor.RenderBorder(inputPosX, inputPosY, maxInputWidth, 1, InfoBoxTitledColor, BackgroundColor) +
                            CsiSequences.GenerateCsiCursorPosition(inputPosX + 2, inputPosY + 2) +
                            $"{ColorTools.RenderSetConsoleColor(InfoBoxTitledColor)}" +
                            $"{ColorTools.RenderSetConsoleColor(BackgroundColor, true)}"
                        );
                    }
                    else
                    {
                        boxBuffer.Append(
                            BorderColor.RenderBorderPlain(inputPosX, inputPosY, maxInputWidth, 1) +
                            CsiSequences.GenerateCsiCursorPosition(inputPosX + 2, inputPosY + 2)
                        );
                    }
                    return boxBuffer.ToString();
                });

                // Render the screen
                ScreenTools.Render();

                // Wait until the user presses any key to close the box
                var settings = new TermReaderSettings()
                {
                    RightMargin = rightMargin,
                };
                if (useColor)
                {
                    settings.InputForegroundColor = InfoBoxTitledColor;
                    settings.InputBackgroundColor = BackgroundColor;
                }
                string input = TermReader.Read("", "", settings, true, true);
                return input;
            }
            catch (Exception ex)
            {
                Debug.WriteLine(ex.StackTrace);
                Debug.WriteLine($"There is a serious error when printing text. {ex.Message}");
            }
            finally
            {
                if (useColor)
                {
                    TextWriterRaw.WriteRaw(
                        ColorTools.RenderSetConsoleColor(ColorTools.CurrentForegroundColor) +
                        ColorTools.RenderSetConsoleColor(ColorTools.CurrentBackgroundColor, true)
                    );
                }
                ConsoleWrapper.CursorVisible = initialCursorVisible;
                ScreenTools.CurrentScreen?.RemoveBufferedPart(infoBoxScreenPart.Id);
                if (initialScreenIsNull)
                    ScreenTools.UnsetCurrent(screen);
            }
            return "";
        }

