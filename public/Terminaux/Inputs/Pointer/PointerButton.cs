using System;

namespace Terminaux.Inputs.Pointer
{
    /// <summary>
    /// Pointer button
    /// </summary>
    [Flags]
    public enum PointerButton
    {
        /// <summary>
        /// No button
        /// </summary>
        None,
        /// <summary>
        /// Left mouse button
        /// </summary>
        Left,
        /// <summary>
        /// Middle mouse button
        /// </summary>
        Middle,
        /// <summary>
        /// Right mouse button
        /// </summary>
        Right,
        /// <summary>
        /// Wheel scrolling up
        /// </summary>
        WheelUp,
        /// <summary>
        /// Wheel scrolling down
        /// </summary>
        WheelDown,
    }
}
